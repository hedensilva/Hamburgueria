    public void atualizaTabela(){
        CargoDAO dao = new CargoDAO();
        ArrayList<Cargo> cargos = dao.listar();
        tab = (DefaultTableModel) tabela.getModel();
        
        while(tab.getRowCount() > 0){
            tab.removeRow(0);
        }
        int i = 0;
        for(Cargo c : cargos){
            tab.addRow(new String[i]);
            
            tab.setValueAt(c.getNomeCargo(),i,0);
            tab.setValueAt(c.getComissao(),i,1);
            tab.setValueAt(c.getId(),i,2);
               
            i++;
        }}
